package ru.mirea.task8.shapes20;

import java.awt.*;

public abstract class Shape {
    protected Color color;
    protected int x;
    protected int y;

    public Color getColor() {
        return color;
    }

    public void setColor(Color color) {
        this.color = color;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
